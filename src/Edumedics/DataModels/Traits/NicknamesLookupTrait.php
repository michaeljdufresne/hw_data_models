    public function nicknameArrayLookup($name){
        $lookup_name=strtoupper($name);
        $keys = [];
        $possible_names = [];
        foreach ($this->nicknames as $row => $nicknames){
            $key = array_search($lookup_name,$nicknames);
            if($key){
                $possible_names = array_merge($possible_names,$nicknames);
            }
        }

        return $possible_names;
    }

    public function storeNicknamesInRedis(){
        foreach ($this->nicknames as $nickNameArray)
        {
            $arraySize = count($nickNameArray);
            for ($i = 0; $i < $arraySize; $i++)
            {
                $newArray = $nickNameArray;
                $key = $newArray[$i];
                unset($newArray[$i]);
                if (Redis::exists('patientNickNames:' . $key))
                {
                    $previousAssociations = json_decode(Redis::get('patientNickNames:' . $key),true);
                    Redis::del('patientNickNames:' . $key);
                    Redis::set('patientNickNames:' . $key, json_encode(array_merge($previousAssociations, $newArray)));
                }
                else
                {
                    Redis::set('patientNickNames:' . $key, json_encode($newArray));
                }
            }
        }
    }

    public function removeNicknamesFromRedis(){
        foreach ($this->nicknames as $nickNameArray)
        {
            $arraySize = count($nickNameArray);
            for ($i = 0; $i < $arraySize; $i++)
            {
                $newArray = $nickNameArray;
                $key = $newArray[$i];
                if (Redis::exists('patientNickNames:' . $key))
                {
                    Redis::del('patientNickNames:' . $key);
                }
            }
        }
    }

    public function nicknameRedisLookup($name){
        $lookup_name=strtoupper($name);
        $possible_names = [];
        if (Redis::exists('patientNickNames:' . $lookup_name)) {
            $possible_names = json_decode(Redis::get('patientNickNames:' . $lookup_name),true);
        }
        return $possible_names;
    }

